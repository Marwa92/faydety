const textReg = /^[a-zA-Z ]*$/;
const emailReg = /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;
const passwordReg = /^(?=.*[\d])(?=.*[A-Za-z])(?=.*[!@#$%^&.*])[\w!@#$%^&.*]{8,}$/;

export { textReg, emailReg, passwordReg };
